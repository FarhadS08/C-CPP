#include<stdio.h>

int main(){
	char value;
	scanf("%c", &value); //We get value from keyboard
	printf("character=%c\n", value); //Print value as character with %c
	printf("decimal=%d\n", value); //Print value as decimal with %d
	printf("octal=%o\n", value); //Print value as octal with %o
	printf("hexadecimal=%x\n", value); //Print value as hex decimal with %x

                
}
